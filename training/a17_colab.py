def main(argv: Sequence[str] | None = None) -> int:
    args = _parser().parse_args(argv)
    try:
        if args.command == "resolve-backbone":
            lock = load_environment_lock(args.lock)
            proof = resolve_backbone_metadata(lock)
            write_json_document(args.output, proof)
            result = {
                "ok": True,
                "output": args.output.as_posix(),
                "resolved_revision": proof["resolved_revision"],
                "resolution_sha256": proof["resolution_sha256"],
            }
        elif args.command == "dry-run":
            receipt = run_dry_receipt(
                lock_path=args.lock,
                preflight_path=args.preflight,
                backbone_path=args.backbone_resolution,
                config_path=args.config,
                dataset_path=args.dataset,
                output_path=args.output,
                receipt_path=args.receipt,
                repository_root=args.repository_root,
            )
            result = {
                "ok": True,
                "receipt": args.receipt.as_posix(),
                "receipt_sha256": receipt["receipt_sha256"],
            }
        elif args.command == "verify":
            summary = verify_evidence(args.evidence_dir, repository_root=args.repository_root)
            result = {
                "ok": True,
                "summary": (args.evidence_dir / "evidence-summary.json").as_posix(),
                "summary_sha256": summary["summary_sha256"],
            }
        elif args.command == "bundle":
            result = bundle_evidence(
                args.evidence_dir,
                repository_root=args.repository_root,
                output=args.output,
            )
        else:
            summary = write_reviewed_evidence_summary(
                args.evidence_dir,
                repository_root=args.repository_root,
                output=args.output,
            )
            result = {
                "ok": True,
                "summary": args.output.as_posix(),
                "summary_sha256": summary["summary_sha256"],
            }
    except (ContractError, OSError) as exc:
        print(json.dumps({"ok": False, "error": str(exc)}, sort_keys=True), file=sys.stderr)
        return 1
    print(json.dumps(result, ensure_ascii=False, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
